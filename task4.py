# タスク 四。二十五

def количество():
    降水量データ = []
    print("3月（31日間）の各日の降水量を入力してください。")

    for イ in range(31):
        降水量データ.append(int(input(f"{イ+1}日: ")))

    カウンタ = 0
    日 = 0

    while 日 < 31:
        if 降水量データ[日] == 0:
            カウンタ += 1
        日 += 1

    return カウンタ

колво = количество()

if колво == 10:
    print("その通りです。ちょうど10日間、雨が全く降りませんでした。")
else:
    print(f"誤り：{колво}日間、降水はありませんでした。")